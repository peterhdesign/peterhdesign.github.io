$( ".previous-arrow" ).mouseenter(function() {
    $(".previous-thumbnail img").css("opacity","1");
  });
  
  $( ".previous-arrow" ).mouseout(function() {
    $(".previous-thumbnail img").css("opacity","0");
  });
  
  $( ".next-arrow" ).mouseenter(function() {
    $(".next-thumbnail img").css("opacity","1");
  });
  
  $( ".next-arrow" ).mouseout(function() {
    $(".next-thumbnail img").css("opacity","0");
  });